#include<stdio.h>

int main(){
    char ch;
    scanf("%c", &ch);
    if(ch == 'h'){
        printf("He\n");
    }else if(ch == 'l'){
        printf("Li\n");
    }else if(ch == 'c'){
        printf("Cao\n");
    }else if(ch == 'd'){
        printf("Duan\n");
    }else if(ch == 'w'){
        printf("Wang\n");
    }else{
        printf("Not Here\n");
    }
    return 0;
}
