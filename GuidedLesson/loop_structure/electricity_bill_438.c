#include<stdio.h>

int main(){
    int n;
    scanf("%d", &n);
    if (0 <= n && n <= 240){
        printf("%.1f\n", 0.4783 * n);
    }else if(n <= 400){
        printf("%.1f\n", 0.4783 * 240 + (n - 240) * 0.5283);
    }else {
        printf("%.1f\n", 0.4783 * 240 + 0.5283 * 160 + (n - 400) * 0.7783);
    }
    return 0;
}
