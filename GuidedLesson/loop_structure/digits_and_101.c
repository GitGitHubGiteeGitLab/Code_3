#include<stdio.h>

int main(){
    int n;
    scanf("%d", &n);
    //得到某个数的个位数字，只需要将这个数字对10取余
    printf("%d\n", n / 1000 + n / 100 % 10 + n / 10 % 10 + n % 10);
    return 0;
}
